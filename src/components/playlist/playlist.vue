<template>
    <transition name="list-fade">
        <div class="playlist" @click="hide" v-show="showFlag">
            <div class="list-wrapper" @click.stop>
                <div class="list-header">
                    <h1 class="title">
                        <i class="icon"></i>
                        <span class="text">{{modeText}}</span>
                        <span class="clear" @click="showConfirm"><i class="icon-clear"></i></span>
                    </h1>
                </div>
                <scroll ref="listContent" :data="sequencelist" class="list-content">
                    <transition-group ref="list" name="list" tag="ul">
                        <li :key="item.id" class="item" v-for="(item,index) in sequencelist"
                            @click="selectItem(item,index)">
                            <i class="current" :class="getCurrentIcon(item)"></i>
                            <span class="text">{{item.name}}</span>
                            <span class="like"><i class="icon-not-favorite"></i></span>
                            <span @click.stop="deleteOne(item)" class="delete"><i class="icon-delete"></i></span>
                        </li>
                    </transition-group>
                </scroll>
                <div class="list-operate">
                    <div @click="addSong" class="add">
                        <i class="icon-add"></i>
                        <span class="text">添加歌曲到队列</span>
                    </div>
                </div>
                <div @click="hide" class="list-close">
                    <span>关闭</span>
                </div>
            </div>
            <confirm ref="confirm" @confirm="confirmClear" text="是否清空播放列表" confirmBtnText="清空"></confirm>
            <add-song ref="addSong"></add-song>
        </div>
    </transition>
</template>


<script type="text/ecmascript-6">
    import {playMode} from '@/common/js/config'
    import {mapGetters,mapActions,mapMutations} from 'vuex'
    import confirm from '@/base/confirm/confirm'
    import scroll from '@/base/scroll/scroll'
    import AddSong from '@/components/add-song/add-song'

    export default {
        data() {
            return {
                showFlag: false
            }
        },
        computed: {
            modeText() {
                return this.mode === playMode.sequence ? '顺序播放' : mode === playMode.random ? '随机播放' : '单曲循环'
            },
            ...mapGetters([
                'mode',
                'sequencelist',
                'currentSong',
                'playlist',
            ])
        },
        methods: {
            showConfirm() {
                this.$refs.confirm.show()
            },
            confirmClear() {
                this.deleteSongList()
                this.hide()
            },
            selectItem(item, index) {
                if (this.mode === playMode.random) {
                    index = this.playlist.findIndex((song) => {
                        return song.id === item.id
                    })
                }
                this.setCurrentIndex(index)
                this.setPlayingState(true)
            },
            addSong() {
                this.$refs.addSong.show()
            },
            deleteOne(item) {
                this.deleteSong(item)
                this.$refs.listContent.refresh()
                if (!this.playlist.length) {
                    this.hide()
                }
            },
            getCurrentIcon(item) {
                return this.currentSong.id === item.id ? 'icon-play' : ''
            },
            hide() {
                this.showFlag = false
            },
            show() {
                this.showFlag = true
                this.$refs.listContent.refresh()
            },
            ...mapActions([
                    'deleteSong',
                    'deleteSongList'
            ]),
            ...mapMutations({
                'setCurrentIndex': 'SET_CURRENT_INDEX',
                'setPlayingState': 'SET_PLAYING_STATE',
            })
        },
        components: {
            confirm,
            scroll,
            AddSong,
        }
    }
</script>

